 private void totales() {
    String pre;
    String can;
    double igv = 0;
    double total = 0;
    double subtotal = 0;
    double precio;
    double cantidad;
    double imp = 0.0;

    for (int i = 0; i < tbdetalle.getRowCount(); i++) {
        pre = tbdetalle.getValueAt(i, 2).toString();
        can = tbdetalle.getValueAt(i, 3).toString();

        precio = Double.parseDouble(pre);
        cantidad = Double.parseDouble(can);

        imp = precio * cantidad;

        subtotal += imp;
        igv = subtotal * 0.18;
        total = subtotal + igv;

        tbdetalle.setValueAt(String.format("%.2f", imp), i, 4);
    }

    txttotal.setText(String.format("%.2f", subtotal));
}
