    private void btnenprocesoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnenprocesoActionPerformed
      
   String codigoPedido = txtcodpedido.getText();
    if (!codigoPedido.isEmpty()) {
        int idPedido = Integer.parseInt(codigoPedido);
        String nuevoEstado = "EN PROCESO";
        
      
        boolean actualizado = negociopedido.actualizarEstadoPedido(idPedido, nuevoEstado);
        
        if (actualizado) {
            JOptionPane.showMessageDialog(null, "Estado del pedido actualizado a 'EN PROCESO'.");
            mostrarpedido("");
       txtcodpedido.setText("");
                    
   colorestado();
   
        } else {
            JOptionPane.showMessageDialog(null, "Error al actualizar el estado del pedido.");
        }
    } else {
        JOptionPane.showMessageDialog(null, "Seleccione un pedido para actualizar su estado.");
    }
        
    }//GEN-LAST:event_btnenprocesoActionPerformed

